using System;
using System.Collections.Generic;
using System.Text;
using System.Text.RegularExpressions;

namespace Ek.Shop.Contracts.Extensions
{
    public static class StringExtensions
    {
        public static bool Contains(this string source, string toCheck, StringComparison comp)
        {
            return source?.IndexOf(toCheck, comp) >= 0;
        }

        public static string ToParameterDictionary(this string source)
        {
            return string.Empty;
        }

        public static string ToCurrencyString(this decimal source)
        {
            return source.ToString("N2");
        }

        public static string RemoveAdditionalSlashes(this string source)
        {
            return Regex.Replace(source, @"/+", @"/");
        }

        public static string GetTextBetween(this string source, string leftWord, string rightWord)
        {
            int n;
            string output = source.Substring(n = 1 + source.IndexOf(leftWord),
                                            source.IndexOf(rightWord) - n);

            return "/" + output;
        }

        /// <summary>
        /// Returns the input string with the first character converted to uppercase, or mutates any nulls passed into string.Empty
        /// </summary>
        public static string FirstLetterToUpperCase(this string s)
        {
            if (string.IsNullOrEmpty(s))
                return string.Empty;

            char[] a = s.ToCharArray();
            a[0] = char.ToUpper(a[0]);
            return new string(a);
        }

        /// <summary>
        /// Returns the input string with the first character converted to lowercase, or mutates any nulls passed into string.Empty
        /// </summary>
        public static string FirstLetterToLowerCase(this string s)
        {
            if (string.IsNullOrEmpty(s))
                return string.Empty;

            char[] a = s.ToCharArray();
            a[0] = char.ToLower(a[0]);
            return new string(a);
        }

        public static List<string> GetInterpolatedStringArguments(this string source)
        {
            var matches = new List<string>();
            var matchCollection = new Regex(@"(?<=\{)[^}]*(?=\})").Matches(source);
            foreach (var match in matchCollection)
            {
                matches.Add(match.ToString().Trim('{', '}'));
            }

            return matches;
        }

        /// <summary>
        /// Produces optional, URL-friendly version of a source, "like-this-one". 
        /// hand-tuned for speed, reflects performance refactoring contributed
        /// by John Gietzen (user otac0n) 
        /// </summary>
        public static string ToUrlFriendly(this string source)
        {
            if (source == null) return null;

            const int maxlen = 80;
            int len = source.Length;
            bool prevdash = false;
            var sb = new StringBuilder(len);
            char c;

            for (int i = 0; i < len; i++)
            {
                c = source[i];
                if ((c >= 'a' && c <= 'z') || (c >= '0' && c <= '9'))
                {
                    sb.Append(c);
                    prevdash = false;
                }
                else if (c >= 'A' && c <= 'Z')
                {
                    // tricky way to convert to lowercase
                    sb.Append((char)(c | 32));
                    prevdash = false;
                }
                else if (c == ' ' || c == ',' || c == '.' || c == '/' ||
                    c == '\\' || c == '-' || c == '_' || c == '=')
                {
                    if (!prevdash && sb.Length > 0)
                    {
                        sb.Append('-');
                        prevdash = true;
                    }
                }
                else if ((int)c >= 128)
                {
                    int prevlen = sb.Length;
                    sb.Append(RemapInternationalCharToAscii(c));
                    if (prevlen != sb.Length) prevdash = false;
                }
                if (i == maxlen) break;
            }

            if (prevdash)
                return sb.ToString().Substring(0, sb.Length - 1);
            else
                return sb.ToString();
        }

